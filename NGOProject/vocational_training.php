            <p>CONTEXT</p>
            <span>
              India will have the world’s youngest population by 2020, with an average age of 29 years and 13 million people entering the workforce each year. However, as the ASER survey has shown, learning outcomes of students completing primary or even secondary schooling are extremely low, closing pathways to senior secondary or higher education. While vocational skills could be a short-term solution, only 2 percent of the workforce in India is currently formally skilled, leaving the vast majority of youth without the skills, knowhow or networks to enter the workforce. This leads to large scale unemployment or under-employment, even as industries are unable to fill their job vacancies.
            </span> <br><br>
            <p>APPROACH</p>
            <!-- <br> -->
            <span>
              "LET'S HELP FOUNDATION" conducts short-term, often residential courses of 2 to 3 months in duration, focusing on young men and women from rural areas and urban slums. The model is based on strong industry linkages and youth counselling and focuses on hands-on skills training. Courses taught include construction (including electrical, plumbing, masonry and welding), hospitality (housekeeping, food and beverage service and food production), automotive mechanic, healthcare nursing and beauty and wellness services. In addition to industry specific skills, students also learn basic English, computer literacy and life skills. "LET'S HELP FOUNDATION" has a four-step approach to its vocational training programs:
            </span><br><hr>
            <span>
              Mobilization<hr>
Training<hr>
Placement<hr>
Post-placement support<br><br>
For youth who are interested in setting up their own enterprises near their homes and to support creation of new jobs, "LET'S HELP FOUNDATION" provides mentoring support to set up micro-enterprises. Up until now, we have supported over 1,500 such youth (primarily women) to set up successful enterprises in the fields of beauty, electrical service and construction.<br><br>

A unique aspect of "LET'S HELP FOUNDATION" vocational training model is the focus on post-placement tracking and support, coupled with financial sustainability. Youth placed in wage employment or supported to set up micro-enterprises are tracked and mentored for a one-year period to encourage them to continue their current jobs, upgrade to new placements and acquire additional skills. Students are further supported to overcome problems of migration through the "LET'S HELP FOUNDATION" Alumni Network, which provides support in the form of accommodation and mess facilities, counselling, social networks, mediation with employers, support in accessing health services, etc. This suite of post-placement services has been instrumental in increasing the retention of students in their first jobs.<br><br>

"LET'S HELP FOUNDATION" has also initiated innovative models to impact the skilling ecosystem. These include an ‘Employment Awareness’ model targeted at adolescent girls and young women, which is implemented in association with the United Nations Development Program (UNDP) and the Maharashtra Government to tackle the low-exposure and lack of information prevalent particularly among rural youth.<br><br>

Migration-support hostels across Delhi, Mumbai and Pune have also been launched to support low-income migrant youth whose first jobs are in these urban areas. Further, several initiatives have been undertaken to increase the sustainability of vocational training through end-user fees and also a ‘business-linked’ training model where trainees use skills to service customers in "LET'S HELP FOUNDATION" hospitality training centre and beauty course.<br><br>

LET'S HELP FOUNDATION's affiliations include National Skill Development Corporation (NSDC) and government sector skill councils. "LET'S HELP FOUNDATION" also has knowledge partnerships with Larsen & Toubro, Tata Motors, and Godrej, among others. LET'S HELP FOUNDATION's vocational program has won awards from the state governments of Madhya Pradesh and Chhattisgarh, as well as the NSDC.<br><br>
            </span><br>
            <p>REACH AND SCALE</p>
            <span>
              "LET'S HELP FOUNDATION" runs 90+ centres across 14 states and 1 Union Territory in India, with a placement rate of over 85 percent. More than 26,000 students will be trained in 2020-2022. Over the past two years LET'S HELP FOUNDATION's vocational training programs have helped nearly 100,000 youth across the country improve general employability skills, and more than 50,000 youth in industry specific trades. We have also developed partnerships with a large number of employers to offer entry-level jobs to our graduates.
