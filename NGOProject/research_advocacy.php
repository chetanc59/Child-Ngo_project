            <p>CONTEXT</p>
            <span>
              The word “aser” (pronounced uh-sir) means “impact” in Hindi and in many Indian languages. If development programs are to lead to desired outcomes, their impact on the ground should be regularly assessed. Large sums of public money are channelled into social sector programs. Lack of information on how these investments translate into outcomes on the ground is a major barrier to evaluating their effectiveness and determining whether taxpayers’ money is being well spent. Currently, independent, timely data on outcomes are not available, especially at the district level. In addition, outside of urban India there is an enormous shortage of people able to design, implement, and understand the nuts and bolts of assessment. ASER Centre was established to fill these critical gaps.
            </span> <br><br>
            <p>APPROACH</p>
            <!-- <br> -->
            <span>
              ASER Centre’s flagship activity is the Annual Status of Education Report (commonly known as the ASER report), a household-based survey that collects information on children’s schooling status and basic learning outcomes in almost every rural district in the country. The ASER survey is an enormous participatory exercise that has involved about 500 organizations and upwards of 25,000 volunteers every year since 2005. Estimates of children’s schooling and learning status are generated at district, state and national levels.<br><br>

ASER is the only annual source of data on children’s learning outcomes available in India today, and is often credited with changing the focus of discussions on education in India from inputs to outcomes. The ASER model has been adapted for use by thirteen other countries across three continents. These countries came together organically to form the People’s Action for Learning (PAL) Network, with a secretariat housed in Nairobi.<br><br>

In addition, ASER Centre implements large- and small-scale research studies addressing a variety of domains both in education and in other social sectors such as health, nutrition, water and sanitation. Conducted with support from institutions such as UNICEF, the World Bank, the MacArthur Foundation and others, these studies seek to generate evidence that is actionable by both policy makers and practitioners.<br><br>

ASER Centre also conducts a range of capacity building workshops on topics related to collecting and using evidence for action, both within the LET'S HELP FOUNDATION network as well as for partner organizations. Key features of all capacity building modules include the integration of classroom and field activities; the constant movement between theory (or policy) and practice (or ground reality); and the use of a variety of classroom techniques (individual, small group and large group activities).
            </span><br><hr>
            
            <p>REACH AND SCALE</p>
            <span>
              The ASER survey reaches almost every rural district in the country and covers more than 15,000 households and 650,000 children each year (the sample size is more than twice that of the survey rounds conducted by the government’s National Sample Survey Organization or NSSO). Otherresearch, assessment and capacity building projects vary in reach and scale.
