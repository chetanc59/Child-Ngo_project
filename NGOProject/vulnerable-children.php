            <p>CONTEXT</p>
            <span>
              "LET'S HELP FOUNDATION" started with a mandate of every child in school and learning well. Within this mission, it was also important to reach and help marginalized and vulnerable children to come into the education net and also provide them with a safety net.<br><br>

"LET'S HELP FOUNDATION" Council focuses on child labour, children with special needs, children in institutions, children suffering from substance abuse, those in conflict with law, children who are victims of sexual abuse and children belonging to the most marginalised communities. Through its work, "LET'S HELP FOUNDATION" Council strives to provide these children with access to education and so that every child has equality of opportunity.
            </span> <br><br>
            <p>APPROACH</p>
            <!-- <br> -->
            <span>
              "LET'S HELP FOUNDATION" Council began as an outreach program in the slum communities of Mumbai, to respond to the needs of children belonging to underserved communities, especially those children who were working and not in school.<br><br>

Currently, "LET'S HELP FOUNDATION" Council's core focus is on child protection issues like children with special needs, child beggars, children suffering from substance abuse, those in conflict with law, children who are victims of sexual abuse and children belonging to the most marginalised communities. "LET'S HELP FOUNDATION" Council strategy includes direct work with children and families in vulnerable communities. Indirect work as a resource organization involves training of different stakeholders working in the field of child protection and partnership with necessary government departments to address these concerns.
            </span><br><hr>
          
            <p>DELIVERY MODELS</p>
            <!-- <br> -->
            <span>
              "LET'S HELP FOUNDATION" Council overall focus is on prevention, remediation, rehabilitation, policy advocacy and documentation for evidence-based practice. "LET'S HELP FOUNDATION" Council addresses the issues of child rights through multipronged interventions.<br><br>

One of the key programs of "LET'S HELP FOUNDATION" Council is to rescue child labourers from exploitative situations. The aim is to repatriate these children back to their homes and seek rehabilitative measures to sustain them in their local and home settings. To facilitate this, various inter-state connections have been established by "LET'S HELP FOUNDATION" Council with the concerned government departments at both source and receiving end. "LET'S HELP FOUNDATION" Council also works at important exit (source) and entry (receiving) points at railway stations in order to mitigate unsafe migration and trafficking of children as a major prevention operation. Another crucial prevention program is the Child Rights Help Desk which is set up at various locations within the community to address the concerns of child rights and for local community members to seek redressal for the same. Child Rights Sessions are also conducted in schools as well as in the community to spread awareness on the child protection concerns.<br><br>

While the focus has been on strengthening children and communities, "LET'S HELP FOUNDATION" Council also has national advocacy-level interventions, from establishing inter-state coordination between various state governments to pushing the issue of child rights and protection to a higher priority on their agenda to accepting ownership for altering significant policy decisions.<br><br>

On the rehabilitation front, "LET'S HELP FOUNDATION" Council provides educational sponsorship for the rescued child labourers. This is for the children who attend the school on regular basis (up to 70% attendance) and it is monitored by "LET'S HELP FOUNDATION" Council staff in consultation with the schools. In the past, "LET'S HELP FOUNDATION" Council has run residential shelters for rescued child labourers in their particular home state as a demonstration of best practise for the State.
